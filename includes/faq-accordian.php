        <li>Larger submersible pumps, handle 65mm and larger solids and normally have a minimum of 80mm discharge. They are generally used in municipal and industrial applications for pumping sewage and all types of industrial wastewater.</li>
      </ul>
      <p>
        Submersible pumps are normally used in a packaged pump station where drainage by gravity is not possible.
